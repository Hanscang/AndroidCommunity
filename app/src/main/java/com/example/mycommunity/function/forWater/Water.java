package com.example.mycommunity.function.forWater;

public class Water {
    /**
     * id : 5
     * brand : 娃哈哈矿泉水
     * price : 25
     */

    private int id;
    private String brand;
    private int price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
